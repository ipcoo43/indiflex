# 변수 Variables ( 정수(int), 실수(float), 논리(bool) )

num = 30    # integer
num = 3.24  # float
3+4*5       # 23
(3+4)*5     # 35
3+4*5-4/2   # 21.0
2**10       # 1024 거듭 제곰
4//2        # 2  몫
4/2         # 2.0 나누기
type(4//2)  # int
type(4/2)   # float
21%2        # 1 나머지 
20>10       # >,<,>=,<=,==,!=
20>10 and 5>=4 # and, or, not
# 오래 걸리는 것을 뒤에 둔다
not 5>10    # True